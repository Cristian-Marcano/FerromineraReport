    //* Validar e insertar el comentario a la DB
    private void btnPublishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPublishActionPerformed
        try {
            ValidateInput.isEmptyOrBlank(List.of(textComment));
            
            String content = textComment.getText();
            int userId = FerromineraProject.user.getId();
            
            CommentService comService = new CommentService();
            comService.createComment(content, userId, reportId);
            
            ScrollReportContent reportsContent = new ScrollReportContent(reportId);
            
            FerromineraProject.contentP.setPanel(reportsContent);
            FerromineraProject.contentP.showPanel();
            
            reportsContent.initReportCommentContent();
            
        } catch(SQLException e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(null,"Ocurrio un Error en la conexi√≥n con la Base de Datos","ERROR",JOptionPane.ERROR_MESSAGE);
        } catch(Exception e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(null,"No se puede avanzar \n" + e.getMessage(),"Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnPublishActionPerformed


